let favoriteSongs = [];

// ... existing code ...

Array.from(document.getElementsByClassName('songItemFavorite')).forEach((element) => {
    element.addEventListener('click', (e) => {
        const songIndex = parseInt(e.target.id);
        
        // Check if the song is already in the favorites
        const isFavorite = favoriteSongs.includes(songIndex);

        if (isFavorite) {
            // Remove from favorites
            const indexToRemove = favoriteSongs.indexOf(songIndex);
            favoriteSongs.splice(indexToRemove, 1);
            e.target.style.color = 'red'; // Change to your default color
        } else {
            // Add to favorites
            favoriteSongs.push(songIndex);
            e.target.style.color = 'green'; // Change to your favorite color
        }

        // You can now use the favoriteSongs array to store the list of favorite songs
        localStorage.favsongs = favoriteSongs;
    });
});
